public class Projector {
    public void on() {
        System.out.println("Projector is ON");
    }

    public void off() {
        System.out.println("Projector is OFF");
    }

    public void setWideScreenMode() {
        System.out.println("Projector is set to WIDE SCREEN mode");
    }
}
